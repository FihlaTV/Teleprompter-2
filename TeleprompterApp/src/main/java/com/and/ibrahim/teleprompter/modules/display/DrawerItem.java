package com.and.ibrahim.teleprompter.modules.display;

public class DrawerItem {

    int progressScrollSpeed;
    int progressTextSize;
    String textFont;

    public String getTextFont() {
        return textFont;
    }

    public void setTextFont(String textFont) {
        this.textFont = textFont;
    }

    public int getProgressTextSize() {
        return progressTextSize;
    }

    public void setProgressTextSize(int progressTextSize) {
        this.progressTextSize = progressTextSize;
    }

    public int getProgressScrollSpeed() {
        return progressScrollSpeed;
    }

    public void setProgressScrollSpeed(int progressScrollSpeed) {
        this.progressScrollSpeed = progressScrollSpeed;
    }


}
